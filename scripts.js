
function openAb() {
	if (document.getElementById("ab_inside").style.display == "block") {
		document.getElementById("ab_inside").style.display = "none";
	} else {
		document.getElementById("ab_inside").style.display = "block";
	}
	
}

function closeAb() {
	document.getElementById("ab_inside").style.display = "none";
} 